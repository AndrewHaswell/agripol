            <p><strong>NB: 1 litre of water weighs 1 Kg</strong></p>

            <p>A small experiment will determine the polymer-to-water ratio to achieve equilibrium. For example, add 1.5
                litres of water to a suitable container and sprinkle 10 grams (about 2 teaspoonsful) of polymer powder
                into the container while agitating the water to prevent lumps from forming.</p>

            <img src="images/1st stage rootdip.jpg" class="agrimg-left"/>

            <p>After about 3 or 4 minutes dip the bare roots of a plant into the gel. If the gel is too thick and too
                much adheres to the roots, add another 250ml of water, stir, wait another 3 or 4 minutes and try again,
                adding more water if necessary until the desired consistency is achieved.</p>

            <img src="images/rootdip stage three cropped.jpg" class="agrimg-right"/>

            <p>If the gel consistency is too thin for adequate root-adherence, add more powder and mix, waiting 3 or 4
                minutes before testing again.</p>

            <p>Then use the same final test ratio, e.g. 1 Kg to 150 litres, for the full scale dipping operation.
                Suitable vessels for making up the gel would be a bucket for 15 - 20 litres or a water butt or tank for
                150 - 200 litres.</p>

            <p><em>Where large numbers of stock are to be treated, this can be accomplished much more quickly by
                    <strong>spraying</strong> the gel onto the bare roots, as follows.</em></p>

            <p>Place the pre-hydrated gel into a sprayer of appropriate size, according to the quantity of stock to be
                treated and with a spray nozzle suitable to allow the gel to flow freely. Set out the stock with the
                roots readily visible, on racking or layered on the ground. Ensure that the gel is of a consistency that
                will permit adequate flow, coverage and penetration amongst the roots. To achieve this, it may be
                necessary to dilute the consistency more than would be suitable for the conventional dipping method of
                treatment.</p>

            <ul>
                <li>Bare-root transplants can be dipped singly or in bundles with equal benefit but ensure the gel
                    penetrates fully amongst the roots of bundled plants.
                </li>
                <li>The ideal time for dipping is as soon after lifting as possible.</li>
                <li>Gel spills should be cleaned up as soon as possible to remove a slippery hazard.</li>
            </ul>


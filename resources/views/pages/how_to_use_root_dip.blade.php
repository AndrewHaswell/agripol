            <p>The right consistency is when the powder has absorbed as much water as it can, with little or no surplus,
                unabsorbed water present. This is called equilibrium.</p>

            <p> The powder will absorb approximately 200 times its own weight of soft water and approximately 150 times
                its own weight of hard water.</p>

